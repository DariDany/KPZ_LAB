class PseudoCode:

    @staticmethod
    def to_pseudocode(lines: str) -> str:
        """
        Перетворює переданий текстовий код у псевдокод.

        Аргументи:
            lines: рядки коду (можуть містити декілька рядків)

        Повертає:
            str: псевдокод у вигляді рядка
        """
        return lines
